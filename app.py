class Game:
    def __init__(self):
        self.screen = pygame.display.set_mode((1280, 720))
        self.clock = pygame.time.Clock()
        self.running = True
        self.dt = 0 
        self.tavern = pygame.image.load('assets/tavern.png').convert_alpha()
        self.blacksmith = pygame.image.load('assets/blacksmith.png').convert_alpha()
        self.background = None
        self.wisp = pygame.sprite.GroupSingle()
        self.wisp.add(Wisp())
        self.player = pygame.sprite.GroupSingle()
        self.player.add(Player())
        self.house1 = pygame.sprite.GroupSingle()
        self.house1.add(House(220, 230))
        self.house2 = pygame.sprite.GroupSingle()
        self.house2.add(House(150, 580))
        self.obstacles = pygame.sprite.Group()

        self.grass_image = pygame.image.load('free_art/Cute_Fantasy_Free/Tiles/Grass_Middle.png')
        self.water_path = 'free_art/Cute_Fantasy_Free/Tiles/Water_Middle.png'

        self.state = 'in overworld'

    
    def make_map(self):
        y = -16
        for row in map:
            y+=16
            x=-16
            for item in row:
                x+=16
                if item == 0:
                    self.screen.blit(self.grass_image, (x,y))

                if item == 1:
                    self.water = pygame.sprite.GroupSingle()
                    self.water.add(Tile(x,y+16, self.water_path))
                    if self.water not in self.obstacles:
                        self.obstacles.add(self.water)
                    self.water.draw(self.screen)


    def run(self):

        while self.running:
            for event in pygame.event.get():
                if event.type == pygame.QUIT:
                    self.running = False
                    

            
            if self.state == 'in overworld':
                self.obstacles.add(self.house1)
                self.obstacles.add(self.house2)
                self.player.sprite.obstacles = self.obstacles

                self.background = pygame.image.load('assets/map.png')
                # self.screen.blit(self.background, (0,0))
                self.wisp.draw(self.screen)
                self.wisp.update(self.dt)
                self.house1.draw(self.screen)
